//var keyword in java
//var keyword is introduced in jdk 10
class VarDemo
{
	public static void main(String args[])
	{
	  var x=101;
	  System.out.println(x);
	  x++;
	  System.out.println(x);
	  
	  var s1="Hi";
	  var s2=" Welcome";

	  var s3=s1+s2;

	  System.out.println(s3);
	}
}